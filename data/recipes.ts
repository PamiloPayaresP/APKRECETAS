export const getRecipesByCategory = (category: string): Recipe[] => {
  return recipes.filter(recipe => recipe.category === category);
};

export const searchRecipes = (filters: {
  query: string;
  category?: string;
  difficulty?: string;
  maxTime?: number;
}): Recipe[] => {
  return recipes.filter(recipe => {
    const matchesQuery = !filters.query || 
      recipe.name.toLowerCase().includes(filters.query.toLowerCase()) ||
      recipe.ingredients.some(ingredient => 
        ingredient.toLowerCase().includes(filters.query.toLowerCase())
      );
    
    const matchesCategory = !filters.category || recipe.category === filters.category.toLowerCase();
    const matchesDifficulty = !filters.difficulty || recipe.difficulty === filters.difficulty;
    const matchesTime = !filters.maxTime || recipe.preparationTime <= filters.maxTime;

    return matchesQuery && matchesCategory && matchesDifficulty && matchesTime;
  });
};

export const getRecipeById = (id: string): Recipe | null => {
  return recipes.find(recipe => recipe._id === id) || null;
};